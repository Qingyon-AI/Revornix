from __future__ import annotations

from datetime import datetime
from typing import Optional

from sqlalchemy import Boolean, DateTime, ForeignKey, Integer, String
from sqlalchemy.orm import Mapped, mapped_column

from common.sql import Base


class EmailUser(Base):
    __tablename__ = 'email_user'

    id: Mapped[int] = mapped_column(Integer, primary_key=True)
    nickname: Mapped[Optional[str]] = mapped_column(String(200))
    user_id: Mapped[Optional[int]] = mapped_column(ForeignKey("user.id"), index=True)
    email: Mapped[Optional[str]] = mapped_column(String(100), index=True)
    hashed_password: Mapped[str] = mapped_column(String(255), nullable=False)
    is_initial_password: Mapped[bool] = mapped_column(Boolean, default=False, nullable=False, comment='Whether it is the initial password generated by the system')
    initial_password: Mapped[Optional[str]] = mapped_column(String(255), comment='The initial password generated by the system')
    has_seen_initial_password: Mapped[bool] = mapped_column(Boolean, default=False, nullable=False, comment='Whether the initial password has been viewed')
    delete_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True))


class User(Base):
    __tablename__ = "user"

    id: Mapped[int] = mapped_column(Integer, primary_key=True)
    uuid: Mapped[str] = mapped_column(String(100), index=True, nullable=False)
    avatar: Mapped[str] = mapped_column(String(500), nullable=False, index=True)
    nickname: Mapped[str] = mapped_column(String(50), index=True, nullable=False)
    last_login_ip: Mapped[Optional[str]] = mapped_column(String(50))
    last_login_time: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True))
    slogan: Mapped[Optional[str]] = mapped_column(String(200))
    gender: Mapped[Optional[int]] = mapped_column(Integer, comment='0: unk, 1: male, 2: female')
    age: Mapped[Optional[int]] = mapped_column(Integer)
    is_forbidden: Mapped[Optional[bool]] = mapped_column(Boolean, default=False)
    default_document_reader_model_id: Mapped[Optional[int]] = mapped_column(ForeignKey("ai_model.id"))
    default_revornix_model_id: Mapped[Optional[int]] = mapped_column(ForeignKey("ai_model.id"))
    default_website_document_parse_user_engine_id: Mapped[Optional[int]] = mapped_column(ForeignKey("user_engine.id"))
    default_file_document_parse_user_engine_id: Mapped[Optional[int]] = mapped_column(ForeignKey("user_engine.id"))
    default_user_file_system: Mapped[Optional[int]] = mapped_column(ForeignKey("user_file_system.id"))
    default_podcast_user_engine_id: Mapped[Optional[int]] = mapped_column(Integer)
    create_time: Mapped[datetime] = mapped_column(DateTime(timezone=True), nullable=False)
    update_time: Mapped[datetime] = mapped_column(DateTime(timezone=True), nullable=False)
    delete_at: Mapped[Optional[datetime]] = mapped_column(DateTime(timezone=True))
